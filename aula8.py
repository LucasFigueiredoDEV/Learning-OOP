# PascalCase - Significa que todas as palavras incjiam com letra maiúscula
# e nada é udade para separá-las, como em:
# MinhaClasse, Classe, MeuObjeto, MeuProgramaMuitoLegal
# Essa é a convenção utilizada para classe em Python;


# camelCase - A única diferença de camelCase para PascalCase é a primeira letra
# sempre será minúcula e o restante das palavras deverá iniciar com letras maiúsculas.
# Como em:
# minhaFuncao, funcaoDeSoma, etc... Essa conversão não é usada em Python.


# snake case - Este é o padrão usado em Python para definir qualquer que não for uma classe. 
# Todas as letras serão minusculas e separadas por um underlie, como em: minha_variavel, funcao_legal, soma.


# Os padrões usados em Python são: snake_case para qualquer coisa e PascalCase para classes.